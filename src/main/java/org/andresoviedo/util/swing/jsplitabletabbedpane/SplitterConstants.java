package org.andresoviedo.util.swing.jsplitabletabbedpane;

/**
 * Splitter constants.
 * 

 */
interface SplitterConstants {

	/**
	 * Indicates that no move has to be performed.
	 */
	public static final int NO_SPLIT = -1;

	/**
	 * Indicates that the tab has to be placed on top.
	 */
	public static final int SPLIT_TOP = 0;

	/**
	 * Indicates that the tab has to be placed at bottom.
	 */
	public static final int SPLIT_BOTTOM = 1;

	/**
	 * Indicates that the tab has to be placed at left.
	 */
	public static final int SPLIT_LEFT = 2;

	/**
	 * Indicates that the tab has to be placed at right.
	 */
	public static final int SPLIT_RIGHT = 3;

}
